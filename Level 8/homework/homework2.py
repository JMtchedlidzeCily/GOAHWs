x = 15
y = 20

print(x == y)   # False: 15 is not equal to 20
print(x != y)   # True: 15 is not equal to 20
print(x >= y)   # False: 15 is less than 20
print(x <= y)   # True: 15 is less than or equal to 20
print(x > y)    # False: 15 is less than 20
print(x < y)    # True: 15 is less than 20
print(x < y and x != y)  # True: 15 is less than 20 and 15 is not equal to 20
print(x > y or x == y)   # False: 15 is less than 20 and 15 is not equal to 20
